public class App {
    public static void main(String[] args) throws Exception {
        // Integer data type
        int num1;
        num1 = 32;
        System.out.println(num1);

        // Float data type
        float f1;
        f1 = 1.5f;
        System.out.println(f1);

        // Double data type
        double d1;
        d1 = 1.5;
        System.out.println(d1);

        // Character data type
        char ch1;
        ch1 = 'H';
        System.out.println(ch1);

        // String data type
        String str1;
        str1 = "Hello";
        System.out.println(str1);
    }
}
